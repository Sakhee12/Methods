class Swapping1
{
	public static void main(String[] args) 
	{
		int a=10;
		int b=20;

		System.out.println("Befor Swapping: ");
		System.out.println("a: "+a);
		System.out.println("b: "+b);

		
		a=b;
		b=a;

		System.out.println("After Swapping: ");
		System.out.println("a: "+a);
		System.out.println("b: "+b);



	}
}
